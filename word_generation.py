def generate_sets_of_words():
    main_forms = {
        f"{prefix}{spelling}{suffix}"
        for spelling in spellings
        for suffix in set.union(suffixes,comp_suffixes)
        for prefix in prefixes
        if not(prefix != "" and suffix in comp_suffixes)
    }
    extra_forms1 = {
        f"{spelling}{skap}{skap_suffix}{man}"
        for spelling in spellings
        for skap_suffix in skap_suffixes
        for man in men
        for skap in skaps
        if not (skap == "" and man != "")
    }
    extra_forms2 = {
        f"{spelling}{skyld}{skyld_suffix}"
        for spelling in spellings
        for skyld_suffix in skyld_suffixes
        for skyld in skylds
    }
    return [main_forms, extra_forms1, extra_forms2]
